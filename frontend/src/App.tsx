/**
 * Acme Data Room - Main Application Component
 * 
 * This is the root component of the Data Room application. It handles:
 * - Authentication state management
 * - Displaying the login screen or main dashboard
 * - File listing and search
 * - Opening the Google Drive file picker
 * 
 * The app uses a simple state machine: loading -> authenticated/unauthenticated
 * 
 * @author Felix Gabriel Girola
 * @created December 24, 2025
 */

import { useState, useEffect } from 'react'
import { FolderOpen, Plus, Search, LogOut, Loader2 } from 'lucide-react'
import { api, type DataroomFile } from './lib/api'
import { Button } from './components/Button'
import { FileCard } from './components/FileCard'
import { DriveFilePicker } from './components/DriveFilePicker'

function App() {
  // Auth state - null means we're still checking
  const [authenticated, setAuthenticated] = useState<boolean | null>(null)
  
  // Files from the data room
  const [files, setFiles] = useState<DataroomFile[]>([])
  const [loading, setLoading] = useState(true)
  
  // Search state
  const [searchQuery, setSearchQuery] = useState('')
  
  // Modal state for the Drive picker
  const [showPicker, setShowPicker] = useState(false)

  // On mount, check auth status and handle OAuth callback
  useEffect(() => {
    // Check if we just completed OAuth - the backend redirects here with ?success=true
    const params = new URLSearchParams(window.location.search)
    if (params.get('success') === 'true') {
      // Clean up the URL so it doesn't look ugly
      window.history.replaceState({}, '', window.location.pathname)
    }

    checkAuth()
  }, [])

  /**
   * Check if user is authenticated with Google Drive
   */
  const checkAuth = async () => {
    try {
      const { authenticated } = await api.getAuthStatus()
      setAuthenticated(authenticated)
      if (authenticated) {
        fetchFiles()
      } else {
        setLoading(false)
      }
    } catch {
      setAuthenticated(false)
      setLoading(false)
    }
  }

  /**
   * Fetch files from the data room, optionally filtered by search query
   */
  const fetchFiles = async () => {
    setLoading(true)
    try {
      const { files } = searchQuery
        ? await api.searchFiles(searchQuery)
        : await api.listFiles()
      setFiles(files)
    } catch (error) {
      console.error('Failed to fetch files:', error)
    } finally {
      setLoading(false)
    }
  }

  /**
   * Initiate the Google OAuth login flow
   */
  const handleLogin = async () => {
    try {
      const { auth_url } = await api.getAuthUrl()
      // Redirect to Google's consent screen
      window.location.href = auth_url
    } catch (error) {
      alert('Failed to initiate login. Please try again.')
    }
  }

  /**
   * Log out and clear the session
   */
  const handleLogout = async () => {
    await api.logout()
    setAuthenticated(false)
    setFiles([])
  }

  /**
   * Handle search form submission
   */
  const handleSearch = (e: React.FormEvent) => {
    e.preventDefault()
    fetchFiles()
  }

  // =========================================================================
  // Render States
  // =========================================================================

  // Still checking authentication status
  if (authenticated === null) {
    return (
      <div className="min-h-screen flex items-center justify-center bg-gradient-to-br from-[hsl(220,90%,98%)] to-[hsl(220,30%,95%)]">
        <Loader2 className="w-10 h-10 animate-spin text-[hsl(var(--primary))]" />
      </div>
    )
  }

  // Not authenticated - show login screen
  if (!authenticated) {
    return (
      <div className="min-h-screen flex items-center justify-center bg-gradient-to-br from-[hsl(220,90%,98%)] to-[hsl(220,30%,95%)]">
        <div className="bg-white rounded-2xl shadow-xl p-8 max-w-md w-full mx-4 text-center">
          {/* Logo/Icon */}
          <div className="w-16 h-16 bg-[hsl(var(--primary))]/10 rounded-2xl flex items-center justify-center mx-auto mb-6">
            <FolderOpen className="w-8 h-8 text-[hsl(var(--primary))]" />
          </div>
          
          <h1 className="text-2xl font-bold text-[hsl(var(--foreground))] mb-2">
            Acme Data Room
          </h1>
          <p className="text-[hsl(var(--muted-foreground))] mb-6">
            Secure document repository for due diligence. Connect your Google Drive to get started.
          </p>
          
          {/* Google Sign In Button */}
          <Button size="lg" onClick={handleLogin} className="w-full">
            <svg className="w-5 h-5" viewBox="0 0 24 24">
              <path
                fill="currentColor"
                d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"
              />
              <path
                fill="currentColor"
                d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"
              />
              <path
                fill="currentColor"
                d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"
              />
              <path
                fill="currentColor"
                d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"
              />
            </svg>
            Connect Google Drive
          </Button>
        </div>
      </div>
    )
  }

  // Authenticated - show main application
  return (
    <div className="min-h-screen bg-gradient-to-br from-[hsl(220,90%,98%)] to-[hsl(220,30%,95%)]">
      {/* Header Bar */}
      <header className="bg-white border-b border-[hsl(var(--border))] sticky top-0 z-40">
        <div className="max-w-6xl mx-auto px-4 py-4 flex items-center justify-between">
          {/* Logo and Title */}
          <div className="flex items-center gap-3">
            <div className="w-10 h-10 bg-[hsl(var(--primary))]/10 rounded-xl flex items-center justify-center">
              <FolderOpen className="w-5 h-5 text-[hsl(var(--primary))]" />
            </div>
            <h1 className="text-xl font-bold text-[hsl(var(--foreground))]">Acme Data Room</h1>
          </div>
          
          {/* Search and Actions */}
          <div className="flex items-center gap-3">
            {/* Search Form */}
            <form onSubmit={handleSearch} className="relative">
              <Search className="absolute left-3 top-1/2 -translate-y-1/2 w-4 h-4 text-[hsl(var(--muted-foreground))]" />
              <input
                type="text"
                placeholder="Search files..."
                value={searchQuery}
                onChange={(e) => setSearchQuery(e.target.value)}
                className="pl-10 pr-4 py-2 w-64 rounded-lg border border-[hsl(var(--border))] focus:outline-none focus:ring-2 focus:ring-[hsl(var(--primary))] focus:border-transparent"
              />
            </form>
            
            {/* Import Button */}
            <Button onClick={() => setShowPicker(true)}>
              <Plus className="w-4 h-4" />
              Import
            </Button>
            
            {/* Logout Button */}
            <Button variant="ghost" onClick={handleLogout} title="Disconnect Google Drive">
              <LogOut className="w-4 h-4" />
            </Button>
          </div>
        </div>
      </header>

      {/* Main Content Area */}
      <main className="max-w-6xl mx-auto px-4 py-8">
        {loading ? (
          // Loading state
          <div className="flex items-center justify-center py-20">
            <Loader2 className="w-10 h-10 animate-spin text-[hsl(var(--primary))]" />
          </div>
        ) : files.length === 0 ? (
          // Empty state
          <div className="text-center py-20">
            <div className="w-20 h-20 bg-[hsl(var(--secondary))] rounded-2xl flex items-center justify-center mx-auto mb-6">
              <FolderOpen className="w-10 h-10 text-[hsl(var(--muted-foreground))]" />
            </div>
            <h2 className="text-xl font-semibold text-[hsl(var(--foreground))] mb-2">
              {searchQuery ? 'No files found' : 'No files in your data room'}
            </h2>
            <p className="text-[hsl(var(--muted-foreground))] mb-6">
              {searchQuery
                ? 'Try a different search term'
                : 'Import files from Google Drive to get started'}
            </p>
            {!searchQuery && (
              <Button onClick={() => setShowPicker(true)}>
                <Plus className="w-4 h-4" />
                Import from Google Drive
              </Button>
            )}
          </div>
        ) : (
          // File grid
          <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-3 gap-4">
            {files.map((file) => (
              <FileCard key={file.id} file={file} onDelete={fetchFiles} />
            ))}
          </div>
        )}
      </main>

      {/* Google Drive File Picker Modal */}
      {showPicker && (
        <DriveFilePicker onClose={() => setShowPicker(false)} onImport={fetchFiles} />
      )}
    </div>
  )
}

export default App
